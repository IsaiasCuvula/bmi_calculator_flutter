import 'package:flutter/material.dart';

TextStyle kTextStyleBold(double size) {
  return TextStyle(
    color: Colors.black,
    fontWeight: FontWeight.w700,
    fontSize: size,
  );
}

TextStyle kTextStyle(double size) {
  return TextStyle(
    color: Colors.black,
    fontWeight: FontWeight.w400,
    fontSize: size,
  );
}
